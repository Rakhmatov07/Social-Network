import { Schema } from "mongoose";

export const UserSchema = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    gender: {
        type: String,
        enum: ['male', 'female'],
        required: true
    },
    country: {
        type: String,
        required: true
    },
    watchedVideos: {
        type: [String]
    }
},
{
    timestamps: true
});